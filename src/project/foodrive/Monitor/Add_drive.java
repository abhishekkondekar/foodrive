            String vol_selection_method=vol_select_method.getSelectedItem().toString();
            
            String query="INSERT INTO drive (drive_id,status,max_volunteers,start_time,area_id,Monitor_id,vol_select_method,end_time) " 
                    + "VALUES (NULL,'Initiated',NULL,?,?,?,?,NULL)";
            
